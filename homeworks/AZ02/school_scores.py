import pandas as pd
import numpy as np

# Шаг 1: Создание DataFrame
# Создаем таблицу из 10 учеников с оценками по 5 предметам
data = {
    "Ученик": ["Иван", "Мария", "Петр", "Анна", "Сергей", "Ольга", "Дмитрий", "Екатерина", "Михаил", "Наталья"],
    "Математика": [5, 4, 3, 5, 4, 2, 3, 4, 5, 3],
    "Русский язык": [4, 5, 4, 3, 5, 3, 2, 4, 5, 4],
    "Физика": [3, 5, 4, 2, 3, 5, 4, 4, 5, 3],
    "Химия": [4, 3, 5, 4, 3, 2, 3, 5, 4, 3],
    "История": [5, 4, 3, 4, 4, 2, 5, 3, 4, 3],
}

df = pd.DataFrame(data)

# Шаг 2: Вывод первых нескольких строк DataFrame
print("Первые строки DataFrame:")
print(df.head())

# Шаг 3: Вычисление средней оценки по каждому предмету
mean_scores = df.iloc[:, 1:].mean()  # берем все столбцы, кроме первого (названия учеников)
print("\nСредние оценки по каждому предмету:")
print(mean_scores)

# Шаг 4: Вычисление медианной оценки по каждому предмету
median_scores = df.iloc[:, 1:].median()
print("\nМедианные оценки по каждому предмету:")
print(median_scores)

# Шаг 5: Вычисление квартилей Q1 и Q3 для оценок по математике
math_scores = df["Математика"]
q1 = np.percentile(math_scores, 25)  # 25-й перцентиль
q3 = np.percentile(math_scores, 75)  # 75-й перцентиль
print(f"\nКвартиль Q1 для математики: {q1}")
print(f"Квартиль Q3 для математики: {q3}")
print(f"Межквартальный размах IQR: {q3-q1}")

# Шаг 6: Вычисление стандартного отклонения
std_deviation = df.iloc[:, 1:].std()
print("\nСтандартное отклонение для каждого предмета:")
print(std_deviation)
