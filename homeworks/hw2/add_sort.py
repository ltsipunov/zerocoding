import random

# Создаем пустой список
numbers = []

# Добавляем в конец списка 3 случайных числа
for _ in range(3):
    numbers.append(random.randint(1, 100))

# Выводим список на экран
print("Список после добавления 3 чисел:", numbers)

# Добавляем в начало списка 2 случайных числа
for _ in range(2):
    numbers.insert(0, random.randint(1, 100))

# Выводим список на экран
print("Список после добавления 2 чисел в начало:", numbers)

# Сортируем список по убыванию
numbers.sort(reverse=True)

# Выводим отсортированный список на экран
print("Список после сортировки по убыванию:", numbers)