# Создаем два множества строк
set1 = {"яблоко", "банан", "апельсин", "груша"}
set2 = {"банан", "киви", "вишня", "апельсин"}

# Выводим множества на экран
print("Первое множество:", set1)
print("Второе множество:", set2)

# Объединение множеств
union_set = set1.union(set2)
print("Объединение множеств:", union_set)

# Пересечение множеств
intersection_set = set1.intersection(set2)
print("Пересечение множеств:", intersection_set)