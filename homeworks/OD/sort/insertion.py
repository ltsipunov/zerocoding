### Insertion Sort

# **Преимущества:**
# - Прост в реализации.
# - Хорошо работает на почти отсортированных данных (время близко к O(n)).
# - Использует минимальную дополнительную память.
#
# **Недостатки:**
# - В худшем случае — O(n²), как и Selection Sort.
# - Неэффективен на больших массивах.

def sort(arr):
    n = len(arr)
    for i in range(1, n):
        key = arr[i]
        j = i - 1
        # Вставляем текущий элемент в отсортированную часть
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1
        arr[j + 1] = key
    return arr