# **Counting Sort (Сортировка подсчётом)**
#
# Описание:
# - Идея: Подсчитать количество вхождений каждого значения в массиве и использовать эти подсчёты для построения отсортированного массива.
# - Работает только с целыми числами в ограниченном диапазоне.
#
# Шаги:
# 1. Определить минимальное и максимальное значение в массиве.
# 2. Создать массив счетчиков для каждого значения в диапазоне.
# 3. Подсчитать количество каждого значения.
# 4. Восстановить отсортированный массив, используя подсчёты.
#
# Сложность:
# - Время: O(n + k), где n — число элементов, k — диапазон значений.
# - Требует дополнительной памяти для массива счетчиков.

def sort(arr):
    if not arr:
        return arr  # Возвращаем пустой массив, если он пустой

    min_val = min(arr)
    max_val = max(arr)
    range_of_values = max_val - min_val + 1

    # Создаем массив счетчиков
    count = [0] * range_of_values

    # Подсчитываем количество каждого элемента
    for num in arr:
        count[num - min_val] += 1

    # Восстанавливаем отсортированный массив
    sorted_arr = []
    for i, cnt in enumerate(count):
        sorted_arr.extend([i + min_val] * cnt)

    return sorted_arr

